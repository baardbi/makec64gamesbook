BasicUpstart2(main)

main:
	jsr $e544	// Clear screen (KERNAL function)

	// ----------------------- Sprite 0 -----------------------
	lda #100 	// X position of sprite 0
	sta $d000
	lda #100 	// Y position of sprite 0
	sta $d001

	lda #$80 	// Sprite pointer index for sprite 0
	sta $07f8	// Sprite pointer for sprite 0

	lda #WHITE	// Make sprite 0 main color white
	sta $d027

	// ----------------------- Sprite 1 -----------------------
	lda #150 	// X position of sprite 1
	sta $d002
	lda #150 	// Y position of sprite 1
	sta $d003

	lda #$81 	// Sprite pointer index for sprite 1
	sta $07f9	// Sprite pointer for sprite 1

	lda #YELLOW	// Make sprite 1 main color yellow
	sta $d028

	// ----------------------- Sprite 2 -----------------------
	lda #180 	// X position of sprite 2
	sta $d004
	lda #180 	// Y position of sprite 2
	sta $d005

	lda #$83 	// Sprite pointer index for sprite 2
	sta $07fa	// Sprite pointer for sprite 2

	lda #BLACK	// Make sprite 2 main color black
	sta $d029

	// ----------------------- Sprite 3 -----------------------
	lda #180 	// X position of sprite 3
	sta $d006
	lda #180 	// Y position of sprite 3
	sta $d007

	lda #$82 	// Sprite pointer index for sprite 3
	sta $07fb	// Sprite pointer for sprite 3

	lda #PURPLE	// Make sprite 3 main color black
	sta $d02a

	// --------------- Settings for all sprites ---------------

	lda #RED	// Set sprite extra color 1 to red
	sta $d025

	lda #GREEN	// Set sprite extra color 2 to green
	sta $d026

	lda #%00001010   // Set sprite 1 & 3 to multicolor
	sta $d01c

	lda #%00001111     // Activate sprite 0, 1, 2 and 3
	sta $d015


gameLoop:

	// Empty and infinite game loop

	jmp gameLoop


*=$2000 "Sprites"	// Sprite graphics starting memory location $2000

